#include "services.h"

int	rehash_config		();

///////////////////////////////////////////////////////////////////////////////
//

int
rehash_config ()
{
	free(config->server);
	free(config->password);
	free(config->port);
	free(config->servname);
	free(config->servdesc);
	free(config->unick);
	free(config->udesc);
	free(config->hnick);
	free(config->hdesc);
	free(config->nnick);
	free(config->ndesc);
	free(config->scnick);
	free(config->scdesc);
	free(config->wnick);
	free(config->wdesc);
	free(config->clonechan);
	free(config->statchan);

	load_config();
	return 0;
}

