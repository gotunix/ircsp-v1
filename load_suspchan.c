int
load_suspchan ()
{
	FILE	*fp, *db;
	char	read[512], *ptr[2];

	fp = fopen("suspchan.db", "r");
	if (!fp)
	{
		db = fopen("suspchan.db", "a");
		fprintf(db, ";Suspended Channel Database\n");
		fclose(db);
		return 0;
	}
	else
	{
		while (fgets(read, 256, fp) != NULL)
		{
			if (read[0] == ';')
			{
				continue;
			}

			ptr[0] = strtok(read, ":");
			ptr[1] = strtok(NULL, "\n");

			addSChan(ptr[0], ptr[1]);
		}
	}
	fclose(fp);
	return 0;
}

